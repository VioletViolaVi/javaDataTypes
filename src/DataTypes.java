public class DataTypes {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println(5);
        System.out.println(11.2);
        System.out.println(true);
        System.out.println(10 % 7);
        System.out.println(7 * 7);
        System.out.println(7 / 7);
        System.out.println(7 - 7);
        System.out.println(7 + 7);
    }
}
